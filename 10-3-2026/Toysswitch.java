public class Toysswitch {
    public static void main(String[] args){
        int toy = 3;

        switch(toy){
            case 1:
                System.out.println("Teddy Bear");
                break;
            case 2:
                System.out.println("Unicorn");
                break;
            case 3:
                System.out.println("Car");
                break;
            case 4:
                System.out.println("Puzzle");
                break;
            default:
                System.out.println("Invalid toy");
        }
    }
    
}
